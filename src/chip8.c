void initialize(chip8 *chip) {
    chip->programCounter = 0x200;
    chip->opcode = 0;
    chip->index = 0;
    chip->stackPointer = 0;
    
    // clearing memory
    
    for (int i = 0; i < 80; i++) {
        memory[i] = fontset[i]; 
    }
}

void emulateCycle(chip8 *chip) {
    chip->opcode = memory[chip->programCounter] << 8 | memory[chip->programCounter + 1];

    switch (chip->opcode & 0xF000) {
        default:
            printf("Unknown OpCode: 0x%X\n", chip->opcode);
    }

    if (chip->delayTimer > 0) {
        --chip->delayTimer;
    }

    if (chip->soundTimer > 0) {
        if (chip->soundTimer == 1) {
            printf("BEEP!\n");
            --chip->soundTimer;
        }
    }
}
